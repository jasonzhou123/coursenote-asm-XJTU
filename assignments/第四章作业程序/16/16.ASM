DATA SEGMENT
	STRING DB 'PASSWORD:$'
	DISPLAY DB '*'
	INPUT DB 4 DUP(?)
	HELLO DB 'HELLO!$'
	SORRY DB 'SORRY!$'
DATA ENDS
CORRECT SEGMENT
	MYCODE DB '1111'
	N DW $ - MYCODE
CORRECT ENDS
STASEG SEGMENT STACK
	DB 10 DUP(?)
STASEG ENDS 
CODE SEGMENT
	ASSUME CS:CODE,DS:DATA,SS:STASEG,ES:CORRECT
START: MOV AX,DATA
	MOV DS,AX
	MOV AX,CORRECT
	MOV ES,AX
	MOV CX, N
	MOV SI,OFFSET INPUT
	MOV BX,SI
	MOV DX, OFFSET STRING
	MOV AH,09H
	INT 21H
	MOV DL,DISPLAY
NEXT:	MOV AH,07H
	INT 21H
	MOV DS:[SI],AL
	INC SI
	MOV AH,02H
	MOV DL,DISPLAY
	INT 21H
	LOOP NEXT
	MOV SI,BX
	MOV DI,OFFSET MYCODE
	MOV CX,4H
	CLD
	REPZ CMPSB
	JNZ WRONG
	MOV DX, OFFSET HELLO
	MOV AH,09H
	INT 21H
	JMP DONE
WRONG:MOV DX, OFFSET SORRY
	MOV AH,09H
	INT 21H
DONE:	JMP $
CODE ENDS
END START