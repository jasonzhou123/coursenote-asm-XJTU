DATA SEGMENT
	NUM DW 2312H,1234H,0A122H,7825H
	M DW ($-NUM)/2-1
	MAX DW ?
DATA ENDS
CODE SEGMENT
ASSUME CS:CODE,DS:DATA
START: MOV AX,DATA
	MOV DS,AX
	LEA SI,NUM
	MOV AX,[SI]
	MOV MAX,AX
	MOV CX, M
	LEA SI,NUM
LAB:  ADD SI,2H
	MOV AX,[SI]
	CMP AX,MAX
	JBE NEXT
	MOV MAX,AX
NEXT: LOOP LAB
	JMP $
CODE ENDS
END START