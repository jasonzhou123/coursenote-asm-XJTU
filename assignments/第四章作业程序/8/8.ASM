DATA SEGMENT
	ZHISHU DB 10 DUP(?)
	N DW 09H
DATA ENDS 
CODE SEGMENT
	ASSUME CS:CODE,DS:DATA
START:MOV AX,DATA
	MOV DS,AX
	MOV DX,2H
	MOV CX,N
	LEA SI,ZHISHU
	MOV [SI],1H
	INC SI
NEXT:	INC DX
	MOV BL,1H
LAB:  INC BL
	MOV AX,DX
	XOR DL,BL
	JZ FIND
	MOV DX,AX
	DIV BL
	TEST AH, 0FFH
	JZ NEXT
	JMP LAB
FIND: MOV [SI],AL
	MOV DL,AL
	INC SI
	LOOP NEXT	
	MOV AX,1H
CODE ENDS
END START